-- CREATE A VIEW NAMED SALES_SUMMARY, SHOWING P_NMAE + TOTAL_SALES
CREATE VIEW SALES_SUMMARY AS 
SELECT P.P_NAME , SUM(P.PRICE*O.QUANTITY) AS TOTAL_SALES
FROM PRODUCTS P
INNER JOIN ORDERS O
ON P.P_ID=O.P_ID
GROUP BY P.P_NAME;

