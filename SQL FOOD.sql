CREATE TABLE CATEGORIES (
CAT_ID INT PRIMARY KEY,
CAT_NAME VARCHAR(50) NOT NULL
);


CREATE TABLE PRODUCTS(
P_ID INT PRIMARY KEY,
P_NAME VARCHAR(50) NOT NULL,
PRICE DECIMAL(8,2),
STOCK INT,
CAT_ID INT,
CONSTRAINT FOREIGN KEY(CAT_ID) REFERENCES CATEGORIES(CAT_ID)
);

